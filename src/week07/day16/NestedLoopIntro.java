package week07.day16;

public class NestedLoopIntro {

    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {//i;0


            for (int j = 0; j < 5; j++) {//5
                System.out.println("Wooden Spoon");

            }


        }
        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");

        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");

        }



    }
}
