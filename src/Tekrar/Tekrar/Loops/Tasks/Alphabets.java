package Tekrar.Tekrar.Loops.Tasks;

public class Alphabets {

    public static void main(String[] args) {


        for(char letter = 'A'; letter <= 'Z'; letter++){
            System.out.print(letter + " ");
        }

        for(char letter = 'Z'; letter >= 'A'; letter--){
            System.out.print(letter + " ");
        }





    }
}
