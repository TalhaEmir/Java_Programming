package Tekrar.Tekrar.StringManipulation.Lesson;

public class CharAt_LengthMethod {
    public static void main(String[] args) {
        String str="Wooden Spoon";

        str.charAt(5);// it takes index number

        System.out.println(str.charAt(5));//n index 5 -- it returns as a char character
        char ch1=str.charAt(5);
        System.out.println(ch1);//n


        str.length();

        int number= str.length();
        System.out.println(number);//12 length not index it is character number

    }


}
