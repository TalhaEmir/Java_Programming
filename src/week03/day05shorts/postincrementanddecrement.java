package week03.day05shorts;

public class postincrementanddecrement {

    public static void main(String[] args) {


//postincrement
        int x= 20;
        System.out.println(x++); // 20
        System.out.println(x);

// postdecrement
        int y= 30;
        System.out.println(y--); //30
        System.out.println(y);






    }
}
