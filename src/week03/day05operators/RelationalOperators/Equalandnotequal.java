package week03.day05operators.RelationalOperators;

public class Equalandnotequal {


    public static void main(String[] args) {

        // ==

        System.out.println(1000==20000);

        System.out.println("java"=="java");

        System.out.println("JAVA"=="java");

     //!=:

        System.out.println(1000!=20000);

        System.out.println("java"!="java");

        System.out.println("JAVA"!="java");
    }
}
